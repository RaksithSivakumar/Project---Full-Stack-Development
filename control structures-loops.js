// Loops

// for loop
for (let i = 0; i < 5; i++) {
    console.log(i);
}

// while loop
let i = 10;
while (i < 20) {
    console.log(i);
    i++;
}

// do-while loop
let j = 0;
do {
    console.log(j);
    j++;
} while (j < 5);

